public class BaiTap2 {
    // Chuyen doi nhiet do
    public static void main(String[] args) {
        // Chuyển từ Celsius sang Fahrenheit
        // Công thức: F = (C × 9/5) + 32
        
        double celsius = 25.0;
        double fahrenheit = (celsius * 9/5) + 32;
        
        System.out.println(celsius + "°C = " + fahrenheit + "°F");
    }
}