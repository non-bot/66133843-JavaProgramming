package bai2_variables;
public class BaiTap1 {
    public static void main(String[] args) {
        // 1. Khai báo các biến
        int a = 15;
        int b = 4;
        double c = 3.5;
        
        // 2. Tính toán
        int tong = a + b;
        double thuong = a / (float)c;
        int phanDu = a;
        
        // 3. In kết quả
        System.out.println("Tổng: " + tong);
        System.out.println("Thương: " + thuong);
        System.out.println("Phần dư: " + phanDu);
    }
}